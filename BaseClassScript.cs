public class BaseClassScript {
    private string className;
    private int damage;
    private double health;
    private int agility;
    private int strenght;
    private int inteligence;

    public string ClassName;
    {
        get {return className;}
        set {className = value;}
    }
    public int Damage
    {
        get {return damage;}
        set {damage = value;}
    }
    public double health{
        get {return health;}
        set {set health;}
    }
    public int agility
    {
        get {return agility;}
        set {set agility;}
    }
    public int strenght
    {
        get {return agility;}
        set {set agility;}
    }
    public int inteligence
    {
        get {return agility;}
        set {set agility;}
    }
}